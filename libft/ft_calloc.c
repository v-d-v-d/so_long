#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*tab;

	tab = malloc(nmemb * size);
	if (tab == 0)
	{
		return (tab);
	}
	ft_bzero(tab, nmemb * size);
	return (tab);
}
